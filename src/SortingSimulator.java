    public void run(){
    
    if(sortType==1){    
        for(int j=1; j<6 ; j++){
            int key = arr[j];
            int i=j-1;
            while((i >=0 ) && (arr[i] > key)){ 
                arr[i+1]=arr[i];
                i--;
                try{        
                    swapTiles(String.valueOf(i+1),String.valueOf(i+1+1));
                }
                catch(InterruptedException e ){
                    System.out.println(e);
                }     
            }
        
        
        arr[i+1] = key;

        }
    }   
        
     if(sortType==2){
     
         for(int i=0 ; i<6; i++ ){
             for(int j=0; j<5; j++){
                 
                 if(arr[j]>arr[j+1]){
                     int temp = arr[j];
                     arr[j] = arr[j+1];
                     arr[j+1] = temp;
                     
                      try{
                            swapTiles(String.valueOf(j),String.valueOf(j+1));
                        }
                            catch(InterruptedException e ){
                            System.out.println(e);
                        }        
                    }
             
             }
         
         }
     
     } 
     
     
    }

