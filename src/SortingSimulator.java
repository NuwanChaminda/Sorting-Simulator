    public void swapTiles(String t_f1, String t_f2) throws InterruptedException{
    tf = fields.get(t_f1);
    tf2 = fields.get(t_f2);
    
    tf2.setBackground(GREEN);
    
        for(int i=0; i<30;i++){
            tf.setLocation(tf.getX(), tf.getY()+1);
            tf2.setLocation(tf2.getX(), tf2.getY()-1);
             thread.sleep(10);
        }
        
        int length = tf2.getX()-tf.getX();
        
        for(int i=0 ; i<length ; i++){
       
            tf.setLocation(tf.getX()+1, tf.getY());
            tf2.setLocation(tf2.getX()-1, tf2.getY());
            thread.sleep(10);
        }
        
        for(int i =0 ; i<30 ; i++){
            tf.setLocation(tf.getX(), tf.getY()-1);
            tf2.setLocation(tf2.getX(), tf2.getY()+1);
             thread.sleep(10);
        
        }
        
      tf2.setBackground(WHITE);
    
//        String temp = tf.getName();
//        tf.setName(tf2.getName());
//        tf2.setName(temp);
        JTextField temp = fields.get(t_f1);
        fields.put(t_f1,tf2);
        fields.put(t_f2,tf);
    }
    
    
